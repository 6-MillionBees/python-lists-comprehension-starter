# Use list comprehension to generate a new list containing the squares of the numbers 1 - 10
squares = [x**2 for x in range(1, 11)]
print(squares)



# Make a new list of names written in title case
last_names = ["johnson", "smith", "williams", "brown", "jones"]

title_case_names = [name.title() for name in last_names]

print(title_case_names)


